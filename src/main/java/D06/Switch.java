package D06;

public class Switch {
    public static void main(String[] args) {

        int sayi = 5;
        switch (sayi) {

            case 1:
                System.out.println("sayi=1");
                break;
            case 2:
                System.out.println("sayi=2");
                break;
            case 3:
                System.out.println("sayı=3");
                break;
            case 4:
                System.out.println("sayı=4");
            default:
                System.out.println("Sayı bunlardan girisi değil");
        }


    }
}
